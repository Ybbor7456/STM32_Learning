int main(void)
{
    HAL_Init();
    SystemClock_Config();

    MX_GPIO_Init();
    MX_TIM1_Init();
    MX_USART2_UART_Init();

    // start the 1ms scheduler tick
    HAL_TIM_Base_Start_IT(&htim1);

    // greet host
    UART_Print("System Booted!!!\r\n");

    while (1)
    {
        if (button_event_flag) {
            button_event_flag = 0;

            // sample the button for ~30 ms so debounce logic can settle
            uint32_t start = msTicks;
            while ((msTicks - start) < 30) {
                Button_Task();
            }
        }

        __WFI();
    }
}

// Only run this when button_event_flag was set
void Button_Task(void)
{
    static uint8_t  debouncedState    = 1;
    static uint8_t  lastRaw           = 1;
    static uint32_t lastChangeMs      = 0;
    static uint8_t  lastReportedState = 1;
    static uint32_t pressCount        = 0;

    uint8_t  raw = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0); // 0 = PRESSED
    uint32_t now = msTicks;

    if (raw != lastRaw) {
        lastRaw = raw;
        lastChangeMs = now;
    }

    if ((now - lastChangeMs) > 20 && raw != debouncedState) {
        debouncedState = raw;

        // detect falling edge: HIGH -> LOW
        if (debouncedState == GPIO_PIN_RESET && lastReportedState == GPIO_PIN_SET) {
            pressCount++;
            HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
            UART_Printf("Button pressed! Count = %lu\r\n", pressCount);
        }

        lastReportedState = debouncedState;
    }
}

