void Button_Task(void)
{
    static uint8_t  debouncedState    = 1;
    static uint8_t  lastRaw           = 1;
    static uint32_t lastChangeMs      = 0;
    static uint8_t  lastReportedState = 1;
    static uint32_t pressCount        = 0;

    uint8_t  raw = HAL_GPIO_ReadPin(button_GPIO_Port, button_Pin); // <— use CubeMX names
    uint32_t now = msTicks;                                        // <— same timebase

    if (raw != lastRaw) {
        lastRaw = raw;
        lastChangeMs = now;
    }

    if ((now - lastChangeMs) > 20 && raw != debouncedState) {
        debouncedState = raw;

        if (debouncedState == GPIO_PIN_RESET && lastReportedState == GPIO_PIN_SET) {
            pressCount++;
            HAL_GPIO_TogglePin(ex_led_GPIO_Port, ex_led_Pin);      // <— same LED
            UART_Printf("Button pressed! Count = %lu\r\n", pressCount);
        }
        lastReportedState = debouncedState;
    }
}


